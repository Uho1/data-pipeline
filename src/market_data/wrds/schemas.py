from __future__ import annotations

from dataclasses import dataclass
from typing import Iterable


@dataclass(frozen=True)
class ColumnDef:
    """Explicit DuckDB column definition."""

    name: str
    duckdb_type: str
    not_null: bool = False


@dataclass(frozen=True)
class TableDef:
    """Explicit DuckDB table definition."""

    name: str
    columns: tuple[ColumnDef, ...]
    primary_key: tuple[str, ...] = ()


def _create_table_sql(table: TableDef) -> str:
    parts: list[str] = []
    for column in table.columns:
        segment = f'"{column.name}" {column.duckdb_type}'
        if column.not_null:
            segment += " NOT NULL"
        parts.append(segment)
    if table.primary_key:
        joined = ", ".join(f'"{name}"' for name in table.primary_key)
        parts.append(f"PRIMARY KEY ({joined})")
    columns_sql = ",\n                ".join(parts)
    return f"""
        CREATE TABLE IF NOT EXISTS "{table.name}" (
                {columns_sql}
        )
    """


