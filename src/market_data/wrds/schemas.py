def create_all_tables_sql() -> Iterable[str]:
    """Yield CREATE TABLE statements for every WRDS table."""

    for table in ALL_TABLES:
        yield _create_table_sql(table)


def table_columns(table_name: str) -> tuple[str, ...]:
    """Return the ordered column names for a table."""

    table = SCHEMA_BY_TABLE[table_name]
    return tuple(column.name for column in table.columns)
